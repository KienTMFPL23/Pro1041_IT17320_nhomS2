    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:\
        
        HoaDon hd = this.getForm();
        
        if (hd == null) {
            return;
        }
       if(this.hds.checkMa(txt_ma.getText().trim()) != null){
           JOptionPane.showMessageDialog(this, "Trùng mã");
           return ;
       }
        if(this.hds.them(hd) >0){
            JOptionPane.showMessageDialog(this,"Thêm thành công");
            loadBang();
            clearfom();
        }
        else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tb_bangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_bangMouseClicked
        // TODO add your handling code here:
        int row = tb_bang.getSelectedRow();
        if (row == -1) {
            return;
        }
        txt_ma.setText((String) tb_bang.getValueAt(row, 0).toString());
        cbbKh.setSelectedItem(tb_bang.getValueAt(row, 1).toString());
        cbbNT.setSelectedItem(tb_bang.getValueAt(row, 2).toString());
        NgayTao.setDate((Date) tb_bang.getValueAt(row, 3));
        ngayThanhToan.setDate((Date) tb_bang.getValueAt(row, 4));
        String tt = tb_bang.getValueAt(row, 5).toString();
        int trangThai = Integer.parseInt(tt);
        if (trangThai == 1) {
            cbbThanhToan.setSelected(true);
        } else {
            cbbThanhToan.setSelected(false);
        }

    }//GEN-LAST:event_tb_bangMouseClicked

