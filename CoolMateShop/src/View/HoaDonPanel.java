    public void loadBang() {
        table = (DefaultTableModel) tb_bang.getModel();
        table.setRowCount(0);
        for (HoaDonViewModel o : this.hds.getlist()) {
            table.addRow(new Object[]{o.getMaHD(),o.getKhachHang(), o.getUsers(),  o.getNgayTao(), o.getNgayThanhToan(), o.getTinhTrang()});
        }
    }

    public void cbbNd() {
        combo = (DefaultComboBoxModel) cbbNT.getModel();
        for (users o : this.urs.getlist()) {
            combo.addElement(o.getHoten());
        }
    }

    public void cbbKH() {
        combo = (DefaultComboBoxModel) cbbKh.getModel();
        for (KhachHang o : this.khs.getList()) {
            combo.addElement(o.getHoTen());
        }
    }

    public HoaDon getForm() {
        String id = "";
        int khachhang = cbbKh.getSelectedIndex();
        int nguoiDung = cbbNT.getSelectedIndex();
        String ma = txt_ma.getText().trim();
        Date ngaytao = NgayTao.getDate();
        Date ngaytt = ngayThanhToan.getDate();
        KhachHang kh = listKh.get(khachhang);
        users us = listND.get(nguoiDung);
        if (ma.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko để trống");
            return null;
        }
        if (ngaytao.equals("") && ngaytt.equals("")) {
            JOptionPane.showMessageDialog(this, "Ch Nhập");
            return null;
        }
        int tt = -1;
        if (cbbThanhToan.isSelected()) {
            tt = 1;
        } else {
            tt = 0;
        }
        HoaDon hd = new HoaDon(id, kh.getId(), us.getId(), ma, ngaytao, ngaytt, tt);
        return hd;
    }
    public void clearfom(){
        txt_ma.setText("");
        NgayTao.setDate(null);
        ngayThanhToan.setDate(null);
        cbbThanhToan.setSelected(false);
    }

