    public void addBang() {
        dtm = (DefaultTableModel) tbTL.getModel();
        dtm.setRowCount(0);
        for (TheLoai tl : this.theLoaiSV.getAll()) {
            dtm.addRow(new Object[]{tl.getMatl(),tl.getTentl()});
        }
    }
    public TheLoai getform(){
       String idStr = "";
        String maStr = txtMa.getText().trim();
        String tenStr = txtTen.getText().trim();
        if (maStr.length() == 0 || tenStr.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được để trống thông tin");
            return null;
        }
        TheLoai tl = new TheLoai(idStr, maStr, tenStr);
        return tl;
    }
    public void clearForm(){
       
        txtMa.setText("");
        txtTen.setText("");
   }

