    private void tbTLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTLMouseClicked
        int row = tbTL.getSelectedRow();
        if (row == -1) {
            return;
        }
       
        txtMa.setText((String) tbTL.getValueAt(row, 0).toString());
        txtTen.setText((String) tbTL.getValueAt(row, 1).toString());
    }//GEN-LAST:event_tbTLMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (theLoaiSV.checkMa(txtMa.getText().trim()) != null) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được trùng mã");
            return;
        }else{
             TheLoai tl = this.getform();
        if (tl == null) {
            return;
        }
        this.theLoaiSV.them(tl);
        addBang();
        clearForm();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tbTL.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa ");
            return;
        }
        TheLoai tl = this.getform();
        if (tl == null) {
            return;
        }
        String idStr = tbTL.getValueAt(row, 0).toString();
        this.theLoaiSV.sua(idStr, tl);
        addBang();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbTL.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xoá");
            return;
        }
        int check = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xoá dòng này?");
        if (check != JOptionPane.YES_OPTION) {
            return;
        }
        String maStr = tbTL.getValueAt(row, 0).toString();
        this.theLoaiSV.xoa(maStr);
        addBang();
        clearForm();
    }//GEN-LAST:event_btnXoaActionPerformed

