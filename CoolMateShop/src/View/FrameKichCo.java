     public void addBang(){
        dtm = (DefaultTableModel)tbKC.getModel();
        dtm.setRowCount(0);
        for (KichCo kc : this.kichCoSV.getAll()) {
            dtm.addRow(new Object[]{ kc.getMa(), kc.getSize()});
        }
    }
    public KichCo getFrom() {
        String idStr = "";
        String maStr = txtMa.getText().trim();
        String sizeStr = txtSize.getText().trim();
        if (maStr.length() == 0 || sizeStr.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được để trống thông tin");
            return null;
        }
        KichCo kc = new KichCo(idStr, maStr, sizeStr);
        return kc;
    }
    public void clearForm() {
        txtMa.setText("");
        txtSize.setText("");
    }
