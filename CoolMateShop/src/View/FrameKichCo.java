    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tbKC.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa ");
            return;
        }

        KichCo kc = this.getFrom();
        if (kc == null) {
            return;
        }
        String maStr = tbKC.getValueAt(row, 0).toString();
        this.kichCoSV.update(maStr, kc);
        addBang();
        clearForm();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
       if (kichCoSV.checkMa(txtMa.getText().trim()) != null) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko được trùng mã");
            return;
        }
        KichCo kc = this.getFrom();
        if (kc == null) {
            return;
        }
        this.kichCoSV.insert(kc);
        addBang();
        clearForm();                                               
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
         int row = tbKC.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xoá");
            return;
        }
        int check = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xoá dòng này?");
        if (check != JOptionPane.YES_OPTION) {
            return;
        }
        String maStr = tbKC.getValueAt(row, 0).toString();
        this.kichCoSV.delete(maStr);
        addBang();
        clearForm();                
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbKCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKCMouseClicked
        int row = tbKC.getSelectedRow();
        if (row == -1) {
            return;
        }
        txtMa.setText((String) tbKC.getValueAt(row, 0).toString());
        txtSize.setText((String) tbKC.getValueAt(row, 1).toString());                                     
    }//GEN-LAST:event_tbKCMouseClicked

